def string_upper(text):
    """
    Возвращает строку с заглавными буквами.
    :param text: str
    :return: str
    """
    return text.upper()


def string_title(text):
    """
    Возвращает строку с заглавными первыми буквами каждого слова в строке.
    :param text: str
    :return: str
    """
    return text.title()


def string_lower(text):
    """
    Возвращает строку в нижнем регистре.
    :param text: str
    :return: str
    """
    return text.lower()

